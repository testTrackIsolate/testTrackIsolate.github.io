function createPresidentBlock(data) {
  const container = document.createElement("div");
  container.className = "president-page";
  container.id=data.name;
  // Grid layout
  const grid = document.createElement("div");
  grid.className = "president-grid";
  if (data.layout === "right") {
    grid.classList.add("reverse");
  }

  // Portrait column
  const portraitCol = document.createElement("div");
  portraitCol.className = "portrait-column";

  const portraitContainer = document.createElement("div");
  portraitContainer.className = "portrait-container";
  portraitContainer.style.width = "300px";
  portraitContainer.style.height = "400px";

  const maskDiv = document.createElement("div");
  maskDiv.className = "portrait-mask";
  maskDiv.style.width = `${data.mask.width}px`;
  maskDiv.style.height = `${data.mask.height}px`;
  maskDiv.style.left = `${data.mask.left}px`;
  maskDiv.style.top = `${data.mask.top}px`;
  maskDiv.style.position = "absolute";
  maskDiv.style.overflow = "hidden";

  const portraitImg = document.createElement("img");
  portraitImg.src = data.portrait;
  portraitImg.alt = `Portrait of ${data.name}`;
  portraitImg.className = "portrait";
  portraitImg.style.position = "absolute";
  portraitImg.style.transformOrigin = "top left";
  portraitImg.style.transform = `scale(${data.transform.scale}) translate(${data.transform.translateX}px, ${data.transform.translateY}px)`;

  const frameImg = document.createElement("img");
  frameImg.src = data.frame;
  frameImg.alt = "Frame overlay";
  frameImg.className = "frame-overlay";

  maskDiv.appendChild(portraitImg);
  portraitContainer.appendChild(maskDiv);
  portraitContainer.appendChild(frameImg);
  portraitCol.appendChild(portraitContainer);

  // Text column
  const textCol = document.createElement("div");
  textCol.className = "text-column";

  const title = document.createElement("h1");
  title.className = "president-title";
  title.textContent = `${data.name} (${data.term})`;

  const fact = document.createElement("div");
  fact.className = "president-fact";
  fact.textContent = data.order;

  const bio = document.createElement("div");
  bio.className = "biography";
  bio.innerHTML = `<p>${data.bio}</p>`;

  textCol.appendChild(title);
  textCol.appendChild(fact);
  textCol.appendChild(bio);

  // Append portrait and text to grid
  if (data.layout === "right") {
    grid.appendChild(portraitCol);
    grid.appendChild(textCol);
  } else{
    grid.appendChild(textCol);
    grid.appendChild(portraitCol);
  }
  
  container.appendChild(grid);

  // Divider
  const hr = document.createElement("hr");
  hr.className = "divider";
  container.appendChild(hr);

  // Quote
  const quoteBlock = document.createElement("blockquote");
  quoteBlock.className = "trump-quote";
  quoteBlock.innerHTML = `
    “${data.quote}”<br>
    <span class="quote-attribution">— President ${data.name}</span>
  `;
  container.appendChild(quoteBlock);

  return container;
}

// Render all presidents
const app = document.getElementById("presidents");
presidentData.forEach(president => {
  const block = createPresidentBlock(president);
  app.appendChild(block);
});
